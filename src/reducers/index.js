function rootReducer(state = initialState, action) {

  //........................................Action VERSION.............................................
    
  if (action.type === "CHOICE_VERSION"){
        if (action.version === "Légende"){
          return {
            ...state,
            carouselImages: state.imagesNoiresLegende,
            price: state.version[1].px,
          }
          
        }
      if (action.version === "Pure"){
        return {
          ...state,
          carouselImages: state.imagesBlanchesPure,
          price: state.version[2].px,

        }
      }
  }

  //........................................Action COLOR.............................................
  
   if (action.type === "CHOICE_COLOR" ){
     
    
    if (action.color === "Teinte métallisée Noir Profond"){
    
        return {...state,
          carouselImagesCouleurs: state.imagesNoiresLegende,
          price: state.couleurs[2].px,
      }
    }
    if (action.color === "Peinture opaque Blanc Glacier"){
     
      return {...state,
        carouselImagesCouleurs: state.imagesBlanchesLegende,
        price: state.couleurs[3].px,

      }
    }
    if (action.color === "Teinte spéciale Bleu Alpine"){
 
      return {...state,
        carouselImagesCouleurs: state.imagesBleuesLegende,
        price: state.couleurs[1].px,

    }
    }

  }

  //........................................Action JANTES.............................................

   if (action.type === "CHOICE_JANTES"){
        if (action.jantes === "legende"){
          return {
            ...state,
            carouselImagesJantes: state.jantesVueLegende,
            price: state.jantes[1].px,

          }
          
        }
      if (action.jantes === "serac"){
        return {
          ...state,
          carouselImagesJantes: state.jantesVueSerac,
          price: state.jantes[2].px,

        }
      }
       if (action.jantes === "standard"){
        return {
          ...state,
          carouselImagesJantes: state.jantesVueStandard,
          price: state.jantes[3].px,


        }
      }
    }

  //........................................Action SELLERIE.............................................

    if (action.type === "CHOICE_SELLERIE"){

      if (action.sellerie === "dinamica"){
        return {
          ...state,
          carouselImagesSellerie: state.cuirNoirDinamicaVue,
          price: state.sellerie[1].px,

        }
        
      }
    if (action.sellerie === "Noir Perfore"){
      return {
        ...state,
        carouselImagesSellerie: state.cuirNoirPerforeVue,
        price: state.sellerie[2].px,

      }
    }
     if (action.sellerie === "Cuir Noir"){
      return {
        ...state,
        carouselImagesSellerie: state.cuirNoirVue,
        price: state.sellerie[3].px,

      }
    }
    if (action.sellerie === "Cuir Brun"){
      return {
        ...state,
        carouselImagesSellerie: state.cuirBrunVue,
        price: state.sellerie[4].px,

      }
    }
  }

    //........................................Action EQUIPEMENTS.............................................

      if (action.type === "CHOICE_EQUIPEMENTS"){
        if (action.equipements === "Design"){
          return {
            ...state,
            carouselImagesEquipements: state.designVue,
          }
          
        }
      if (action.equipements === "Média et Navigation"){
          
        return {
          ...state,
          carouselImagesEquipements: state.mediaVue,

        }
      }
       if (action.equipements === "Confort"){
        return {
          ...state,
          carouselImagesEquipements: state.confortVue,

        }
      }  
      if (action.equipements === "Conduite"){
        return {
          ...state,
          carouselImagesEquipements: state.conduiteVue,

        }
      }
      if (action.equipements === "Sécurité"){
        return {
          ...state,
          carouselImagesEquipements: state.securiteVue,

        }
      }
      if (action.equipements === "Personnalisation Extérieure"){
        return {
          ...state,
          carouselImagesEquipements: state.exterieurVue,

        }
      }
      if (action.equipements === "Personnalisation Intérieure"){
        return {
          ...state,
          carouselImagesEquipements: state.interieurVue,

        }
      }
    }
    //........................................Action EQUIPEMENTS PRICE.............................................

    if (action.type === "CHOICE_EQUIPEMENTS_PRICE"){
      if (action.mediaVue === "Alpine Télémetrics"){
        return {
          ...state,
          price: state.mediaVue[1].px,
        }
        
      }
      if (action.mediaVue === "Système Audio Focal"){
        return {
          ...state,
          price: state.mediaVue[2].px,
        }
        
      }
      if (action.mediaVue === "Système Audio Focal Premium"){
        return {
          ...state,
          price: state.mediaVue[3].px,
        }
        
      }
      if (action.mediaVue === "Système Audio standard"){
        return {
          ...state,
          price: state.mediaVue[4].px,
        }
        
      }

    }
      //........................................Action ACCESSOIRES.............................................

      if (action.type === "CHOICE_ACCESSOIRES"){

        if (action.accessoires === "Option Transport"){
          return {
            ...state,
            carouselImagesAccessoires: state.transportVue,
           
          }          
        }

      if (action.accessoires === "Options Multimédia"){
        return {
          ...state,
          carouselImagesAccessoires: state.multimediaVue,

        }
      }

       if (action.accessoires === "Options Matériels"){
        return {
          ...state,
          carouselImagesAccessoires: state.materielVue,
        }
      }

      if (action.accessoires === "Options Intérieur"){
        return {
          ...state,
          carouselImagesAccessoires: state.interieurVue,
        }
      }

      if (action.accessoires === "Options Extérieur"){
        return {
          ...state,
          carouselImagesAccessoires: state.exterieurVue,
        }
    }
  }

  return state 
}

 
